--  DDL for View VW_ORDENES_ACTIVAS
--------------------------------------------------------

  CREATE OR REPLACE FORCE EDITIONABLE VIEW "BD_RESTAURANTE"."VW_ORDENES_ACTIVAS" ("ORDEN_ID", "MESA_NUMERO", "MOZO", "ESTADO", "FECHA_APERTURA", "TOTAL_ITEMS", "SUBTOTAL") AS 
  SELECT O.ID                                       AS ORDEN_ID,
             M.NUMERO                                   AS MESA_NUMERO,
             P.NOMBRES || ' ' || P.APELLIDOS            AS MOZO,
             O.ESTADO,
             O.FECHA_APERTURA,
             COUNT (OD.ID)                              AS TOTAL_ITEMS,
             SUM (OD.CANTIDAD * OD.PRECIO_UNITARIO)     AS SUBTOTAL
        FROM ORDEN O
             JOIN MESA M ON M.ID = O.MESA_ID
             JOIN USUARIO U ON U.ID = O.MOZO_ID
             JOIN PERSONA P ON P.ID = U.PERSONA_ID
             LEFT JOIN ORDEN_DETALLE OD ON OD.ORDEN_ID = O.ID
       WHERE O.ESTADO NOT IN ('CERRADA', 'PAGADA')
    GROUP BY O.ID,
             M.NUMERO,
             P.NOMBRES,
             P.APELLIDOS,
             O.ESTADO,
             O.FECHA_APERTURA
;
--------------------------------------------------------
--  DDL for View VW_PRODUCTOS_MAS_VENDIDOS
--------------------------------------------------------

  CREATE OR REPLACE FORCE EDITIONABLE VIEW "BD_RESTAURANTE"."VW_PRODUCTOS_MAS_VENDIDOS" ("NOMBRE", "CATEGORIA", "TOTAL_VENDIDO", "TOTAL_INGRESOS") AS 
  SELECT PR.NOMBRE,
             C.NOMBRE                                   AS CATEGORIA,
             SUM (OD.CANTIDAD)                          AS TOTAL_VENDIDO,
             SUM (OD.CANTIDAD * OD.PRECIO_UNITARIO)     AS TOTAL_INGRESOS
        FROM ORDEN_DETALLE OD
             JOIN PRODUCTO PR ON PR.ID = OD.PRODUCTO_ID
             LEFT JOIN CATEGORIA_PRODUCTO C ON C.ID = PR.CATEGORIA_ID
    GROUP BY PR.NOMBRE, C.NOMBRE
    ORDER BY TOTAL_VENDIDO DESC
;
--------------------------------------------------------
--  DDL for View VW_VENTAS_DIA
--------------------------------------------------------

  CREATE OR REPLACE FORCE EDITIONABLE VIEW "BD_RESTAURANTE"."VW_VENTAS_DIA" ("FECHA", "TOTAL_ORDENES", "TOTAL_INGRESOS") AS 
  SELECT TRUNC (PA.FECHA_PAGO)     AS FECHA,
             COUNT (*)                 AS TOTAL_ORDENES,
             SUM (PA.TOTAL)            AS TOTAL_INGRESOS
        FROM PAGO PA
       WHERE PA.PAGADO = TRUE
    GROUP BY TRUNC (PA.FECHA_PAGO)
    ORDER BY FECHA DESC
;
